export function getThemeColor (theme: string, color: string) {
  const currentTheme = document.querySelector(`[data-theme=${theme}]`)
  return hslStrToHex(getComputedStyle(currentTheme || document.documentElement).getPropertyValue(color))
}

export function getBgColor (theme: string) {
  return getThemeColor(theme, '--b1')
}

export function getButtonRoundPx (theme: string): number {
  const currentTheme = document.querySelector(`[data-theme=${theme}]`)
  const rr = getComputedStyle(currentTheme || document.documentElement).getPropertyValue('--rounded-btn')
  return remToPx(rr, getFontSize(theme))
}

export function getFontSize (theme: string): number {
  const currentTheme = document.querySelector(`[data-theme=${theme}]`)
  return parseFloat(getComputedStyle(currentTheme || document.documentElement).fontSize)
}


export function getLightLevel(color: string) {
  const hex = color.replace('#', '');
  const c_r = parseInt(hex.substring(0, 0 + 2), 16);
  const c_g = parseInt(hex.substring(2, 2 + 2), 16);
  const c_b = parseInt(hex.substring(4, 4 + 2), 16);
  return ((c_r * 299) + (c_g * 587) + (c_b * 114)) / 1000;
}

export function hslStrToHex(str: string) {
  const arr = str.split(' ');
  if(arr.length != 3) return undefined;
  let h = parseInt(arr[0]), s=parseInt(arr[1]), l=parseInt(arr[2]);

  l /= 100;
  const a = s * Math.min(l, 1 - l) / 100;
  const f = (n: number) => {
    const k = (n + h / 30) % 12;
    const color = l - a * Math.max(Math.min(k - 3, 9 - k, 1), -1);
    return Math.round(255 * color).toString(16).padStart(2, '0');   // convert to Hex and prefix "0" if needed
  };
  return `#${f(0)}${f(8)}${f(4)}`;
}

export function remToPx(rem: string, fontSize: number) {
  const r = parseFloat(rem.replace('rem', ''))
  return r * fontSize
}


